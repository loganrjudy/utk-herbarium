								  <p><STRONG></STRONG>TENN herbarium databases are updated frequently for online use. Searching or browsing results from theVascular Plants occurrence  database are listed alphabetically in  full scientific name by each taxon in either family name or genus name  order. Common name(s) and synonym(s) if any will be listed beneath each  scientific name in the name list. Each genus name heading also  contains links to view photographs and/or distribution maps of the taxon. All species  listed are also hyperlinked to give the same  information mentioned above.</p>
