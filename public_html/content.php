									<p>Online publications are done via real-time ODBC connections.  Search queries are performed based on the Structured Query Language  (SQL)  generated on the fly and directly accesses our most current online  database. A select few pages also requires the use of client side  JavaScript  so please ensure your browse supports this feature.</p>
									<p>Searching or browsing the Fungus collections database will yield a list of all taxa  that meets the fungus criteria. The results are listed alphabetically in  the full scientific name based on location of collection or type. Beneath  each name listing, a taxon's location and date of collection will be  printed. Accession numbers are also listed with each record set.  Searching by genus name is allowed under both search by country and browse by  type.</p>
