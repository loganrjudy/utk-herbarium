         <header class="entry-header"><h1 class="entry-title">Welcome to the UT Herbarium - TENN</h1></header>
									
									<p>The nationally-recognized University of Tennessee Herbarium (TENN) houses over 649,000 accessioned specimens of vascular plants (ferns, cone-bearing, and flowering plants), bryophytes (mosses and liverworts), fungi, lichens, and macroalgae. We are an official repository for the Great Smoky Mountains National Park (a <a href="https://whc.unesco.org/en/list/259/" target="new">UNESCO World Heritage Site</a> and part of the <a href="https://en.unesco.org/biosphere/eu-na/southern-appalachian" target="new">Southern Appalachian Biosphere Reserve</a>) and we are the primary repository of the native and naturalized plants and fungi of the state of Tennessee. It is the largest collection from the southern Appalachians and has a strong emphasis on the eastern US, Mexico, and Central America.</p>
				
									<p>In 2014, TENN moved into the newly-renovated Temple Hall, occupying the entire first floor of the building. We have over 4,500 sq. ft. of collections space, as well as offices, a library, and separate mounting and photography rooms. Our modern climate control system ensures the longevity of these valuable specimens. Our address is <a href="https://www.google.com/maps/place/1818+Andy+Holt+Ave,+Knoxville,+TN+37916/@35.952691,-83.9343748,17z/data=!3m1!4b1!4m5!3m4!1s0x885c17e1fe200565:0x4ab6c15611d52386!8m2!3d35.952691!4d-83.9321861" target="new"><strong>1818 Andy Holt Ave</strong>.</a> in Knoxville, Tennessee, next to the Clarence Brown Theater. </p>
									<p>Our <STRONG><a href="vascular/index.php">vascular plant collection</a></STRONG> is unique in having the largest collection of specimens from the state of Tennessee as well as the historic collections documenting the flora of the Great Smoky Mountains National Park. The vascular plant website hosts nearly 7,000 photos and distribution maps of Tennessee plants. Our <STRONG><a href="bryophyte/index.php">bryophyte collection</a></STRONG> houses 183,000 filed specimens from throughout the world, with a strong emphasis on species found in Tennessee [Appalachian Region], North America [Pacific Northwest and Alaska, Southeast US], Mexico, and Asia. Our <STRONG><a href="fungus/index.php">fungal collection</a></STRONG> houses over 74,000 searchable records online, including ca. 6,000 collections of plant pathogens. This world-wide collection has a strong emphasis on specimens of China and New Zealand, and no other collection can match TENN in southern Appalachian fungi.</p>
									<p>TENN is committed to bringing its collections fully available and online. The fungal herbarium is already completely online, and the moss/ lichen herbarium is undergoing barcoding (a record of each specimen is being created and linked to a label image). Research notebooks belonging to Dean L. R. Hesler (for whom Hesler Biology Building was named) have been digitized and are available through the UT Library website. TENN is associated with the Southeast Regional Network of Expertise and Collections, which is devoted to bringing the resources of nearly 150 regional herbaria of the Southeast together into one standardized, researchable database.</p>
									<p>Our state-supported facility is used for undergraduate, graduate, and professional teaching, research, and public service. It maintains active loan, exchange and gift programs, and receives and provides financial support to visitors. In addition, the general public and numerous state and federal agencies, conservation groups, environmental consultants, and legal counsel rely on our specimens for general information (identifications, poisonous plants, endangered species, exotics, etc.) and for data critical to biodiversity, habitat management, and conservation decisions.</p>

